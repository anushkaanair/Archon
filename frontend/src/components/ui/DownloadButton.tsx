import { useState } from 'react';
import { Download, Loader2, FileJson, FileText } from 'lucide-react';

interface DownloadButtonProps {
  blueprintData: any;
  inputText: string;
}

export default function DownloadButton({ blueprintData, inputText }: DownloadButtonProps) {
  const [loadingPdf, setLoadingPdf] = useState(false);

  const downloadJson = () => {
    const blob = new Blob([JSON.stringify(blueprintData, null, 2)], {
      type: 'application/json',
    });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = `archon-blueprint-${Date.now()}.json`;
    a.click();
    URL.revokeObjectURL(url);
  };

  const downloadPdf = async () => {
    setLoadingPdf(true);
    try {
      const { jsPDF } = await import('jspdf');
      const doc = new jsPDF({ unit: 'mm', format: 'a4' });

      const margin = 15;
      let y = margin;
      const pageW = doc.internal.pageSize.getWidth();
      const maxW = pageW - margin * 2;

      // Header
      doc.setFillColor(13, 11, 24);
      doc.rect(0, 0, pageW, 30, 'F');
      doc.setTextColor(255, 255, 255);
      doc.setFontSize(18);
      doc.setFont('helvetica', 'bold');
      doc.text('Archon — AI Architecture Blueprint', margin, 20);
      y = 40;

      // Input
      doc.setTextColor(120, 115, 180);
      doc.setFontSize(9);
      doc.setFont('helvetica', 'normal');
      doc.text('PRODUCT IDEA', margin, y);
      y += 6;
      doc.setTextColor(40, 40, 40);
      doc.setFontSize(11);
      const inputLines = doc.splitTextToSize(inputText, maxW);
      doc.text(inputLines, margin, y);
      y += inputLines.length * 6 + 8;

      // Detected Tasks
      if (blueprintData.detected_tasks?.length) {
        doc.setTextColor(120, 115, 180);
        doc.setFontSize(9);
        doc.text('DETECTED AI TASKS', margin, y);
        y += 6;
        doc.setTextColor(40, 40, 40);
        doc.setFontSize(10);
        blueprintData.detected_tasks.forEach((t: any) => {
          doc.text(`• ${t.task} (confidence: ${(t.confidence * 100).toFixed(0)}%)`, margin + 4, y);
          y += 6;
        });
        y += 4;
      }

      // Models
      if (blueprintData.recommendations?.length) {
        doc.setTextColor(120, 115, 180);
        doc.setFontSize(9);
        doc.text('RECOMMENDED MODELS', margin, y);
        y += 6;
        blueprintData.recommendations.slice(0, 5).forEach((r: any, i: number) => {
          doc.setTextColor(40, 40, 40);
          doc.setFontSize(10);
          doc.text(
            `${i + 1}. ${r.model_name} (${r.provider}) — score: ${r.scores?.composite?.toFixed(2) ?? 'N/A'}`,
            margin + 4, y
          );
          y += 6;
        });
        y += 4;
      }

      // Cost
      if (blueprintData.cost_estimate) {
        doc.setTextColor(120, 115, 180);
        doc.setFontSize(9);
        doc.text('COST ESTIMATE', margin, y);
        y += 6;
        doc.setTextColor(40, 40, 40);
        doc.setFontSize(11);
        doc.setFont('helvetica', 'bold');
        doc.text(
          `Total Monthly: $${blueprintData.cost_estimate.total_monthly_usd?.toFixed(2) ?? '—'}`,
          margin + 4, y
        );
        y += 8;
        doc.setFont('helvetica', 'normal');
      }

      // Latency
      if (blueprintData.latency_estimate) {
        doc.setTextColor(120, 115, 180);
        doc.setFontSize(9);
        doc.text('LATENCY ESTIMATE', margin, y);
        y += 6;
        doc.setTextColor(40, 40, 40);
        doc.setFontSize(11);
        doc.setFont('helvetica', 'bold');
        doc.text(
          `Total P95: ${blueprintData.latency_estimate.total_p95_ms ?? '—'}ms`,
          margin + 4, y
        );
        y += 8;
        doc.setFont('helvetica', 'normal');
      }

      // Explanation
      if (blueprintData.explanation) {
        if (y > 220) { doc.addPage(); y = margin; }
        doc.setTextColor(120, 115, 180);
        doc.setFontSize(9);
        doc.text('ARCHITECTURE EXPLANATION', margin, y);
        y += 6;
        doc.setTextColor(40, 40, 40);
        doc.setFontSize(9);
        const expLines = doc.splitTextToSize(blueprintData.explanation, maxW);
        doc.text(expLines, margin, y);
      }

      // Footer
      const pageCount = (doc.internal as any).getNumberOfPages();
      for (let p = 1; p <= pageCount; p++) {
        doc.setPage(p);
        doc.setTextColor(180, 180, 180);
        doc.setFontSize(8);
        doc.text(
          `Generated by Archon | ${new Date().toLocaleDateString()} | Page ${p}/${pageCount}`,
          pageW / 2, doc.internal.pageSize.getHeight() - 8,
          { align: 'center' }
        );
      }

      doc.save(`archon-blueprint-${Date.now()}.pdf`);
    } catch (e) {
      console.error('PDF generation failed', e);
    } finally {
      setLoadingPdf(false);
    }
  };

  return (
    <div className="flex items-center gap-3">
      <button
        onClick={downloadJson}
        className="flex items-center gap-2 h-9 px-4 bg-white/5 hover:bg-white/10 border border-white/10 rounded-lg text-sm font-medium text-white transition-colors"
      >
        <FileJson className="w-4 h-4 text-archon-mist" />
        Export JSON
      </button>
      <button
        onClick={downloadPdf}
        disabled={loadingPdf}
        className="flex items-center gap-2 h-9 px-4 bg-archon-core hover:bg-archon-bright disabled:opacity-60 rounded-lg text-sm font-medium text-white transition-colors shadow-[0_0_15px_rgba(83,74,183,0.3)]"
      >
        {loadingPdf ? (
          <Loader2 className="w-4 h-4 animate-spin" />
        ) : (
          <Download className="w-4 h-4" />
        )}
        Export PDF
      </button>
    </div>
  );
}
